def dice_score(p, l, eps=1e-5):
    return (2.*(p*l).sum() / (p.sum()+l.sum()+eps)).item()

def hausdorff_95(pred_np, label_np):
    if not pred_np.any() and not label_np.any(): return 0.0
    if not pred_np.any() or not label_np.any():  return np.inf
    d1 = distance_transform_edt(~pred_np)[label_np]
    d2 = distance_transform_edt(~label_np)[pred_np]
    return float(np.percentile(np.concatenate([d1, d2]), 95))

def sens_spec(p, l):
    tp = int((p*l).sum())
    fp = int((p*(1-l)).sum())
    fn = int(((1-p)*l).sum())
    tn = int(((1-p)*(1-l)).sum())
    return tp/(tp+fn+1e-8), tn/(tn+fp+1e-8)

def extract_peds_regions(oh):
    """oh: (B,5,H,W,D) one-hot → WT, TC, ET binary masks"""
