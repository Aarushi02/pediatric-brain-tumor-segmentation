def find_files(patient_dir):
    pid = patient_dir.name
    files = {}
    for mod in MODALITIES:
        p = patient_dir / f'{pid}-{mod}.nii.gz'
        files[mod] = p if p.exists() else None
    seg_p = patient_dir / f'{pid}-seg.nii.gz'
    files['seg'] = seg_p if seg_p.exists() else None
    return files

def load_vol(path):
    return nib.load(str(path)).get_fdata().astype(np.float32)

