class CheckpointManager:
    def __init__(self, ckpt_dir, top_k=3):
        self.dir    = Path(ckpt_dir)
        self.dir.mkdir(parents=True, exist_ok=True)
        self.top_k  = top_k
        self.scores = []

    def save(self, model, optimizer, epoch, metrics):
        score = metrics['dice_mean']
        path  = self.dir / f'epoch{epoch:04d}_dice{score:.4f}.pth'
        torch.save({'epoch': epoch, 'model': model.state_dict(),
                    'optimizer': optimizer.state_dict(), 'metrics': metrics}, path)
        self.scores.append((score, path))
        self.scores.sort(key=lambda x: x[0], reverse=True)
        while len(self.scores) > self.top_k:
            _, old = self.scores.pop()
            if old.exists(): old.unlink()
        return path

    def best(self):
        return self.scores[0] if self.scores else (0.0, None)

