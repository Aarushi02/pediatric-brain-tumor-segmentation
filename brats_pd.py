def train_epoch(model, loader, optimizer, loss_fn, scaler):
    model.train()
    total_loss = 0.0
    for batch in tqdm(loader, desc='  Train', leave=False):
        imgs   = batch['image'].to(DEVICE)
        labels = batch['label'].to(DEVICE)
        optimizer.zero_grad()
        with torch.cuda.amp.autocast(enabled=CFG['amp']):
            loss = loss_fn(model(imgs), labels)
        scaler.scale(loss).backward()
        scaler.unscale_(optimizer)
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        scaler.step(optimizer)
        scaler.update()
        total_loss += loss.item()
    return total_loss / len(loader)


@torch.no_grad()
def validate(model, loader, loss_fn):
    model.eval()
    val_loss, wt_d, tc_d, et_d = 0.0, [], [], []
    for batch in tqdm(loader, desc='  Val  ', leave=False):
        imgs   = batch['image'].to(DEVICE)
        labels = batch['label'].to(DEVICE)
        preds  = sliding_window_inference(
            imgs, CFG['roi_size'], CFG['sw_batch_size'],
            model, overlap=CFG['overlap'])
        val_loss += loss_fn(preds, labels).item()
        for p, l in zip([post_pred(x) for x in decollate_batch(preds)],
                        [post_label(x) for x in decollate_batch(labels)]):
            d = compute_peds_dice(p.unsqueeze(0), l.unsqueeze(0))
            wt_d.append(d['dice_wt'])
            tc_d.append(d['dice_tc'])
            et_d.append(d['dice_et'])
    return {
        'val_loss':  val_loss / len(loader),
        'dice_wt':   np.mean(wt_d),
        'dice_tc':   np.mean(tc_d),
        'dice_et':   np.mean(et_d),
        'dice_mean': np.mean(wt_d + tc_d + et_d),
    }

