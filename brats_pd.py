def compute_peds_dice(pred_oh, label_oh):
    def dice(p, l):
        i = (p*l).sum()
        return (2.*i / (p.sum()+l.sum()+1e-5)).item()

    wt_p = ((pred_oh[:,1]+pred_oh[:,2]+pred_oh[:,3]+pred_oh[:,4])>0).float()
    wt_l = ((label_oh[:,1]+label_oh[:,2]+label_oh[:,3]+label_oh[:,4])>0).float()

    tc_p = ((pred_oh[:,1]+pred_oh[:,3])>0).float()
    tc_l = ((label_oh[:,1]+label_oh[:,3])>0).float()

    et_p = pred_oh[:,1].float()
    et_l = label_oh[:,1].float()

    return {'dice_wt': dice(wt_p, wt_l),
            'dice_tc': dice(tc_p, tc_l),
            'dice_et': dice(et_p, et_l)}

