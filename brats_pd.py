def build_file_list(data_dir: str) -> List[Dict]:
    """
    Scans DATA_DIR for patients that have ALL 4 modalities + seg mask.
    File naming: BraTS-PED-00XXX-000-{mod}.nii.gz
    """
    records = []
    data_path = Path(data_dir)

    for pdir in sorted(data_path.iterdir()):
        if not pdir.is_dir():
            continue
        pid = pdir.name
        entry = {'image': [], 'label': None}


        all_found = True
        for mod in MODALITIES:
            exact = pdir / f'{pid}-{mod}.nii.gz'
            if exact.exists():
                entry['image'].append(str(exact))
            else:

                matches = list(pdir.glob(f'*-{mod}.nii.gz'))
                if matches:
                    entry['image'].append(str(matches[0]))
                else:
                    all_found = False
                    break


        seg_exact = pdir / f'{pid}-seg.nii.gz'
        if seg_exact.exists():
            entry['label'] = str(seg_exact)
        else:
            seg_matches = list(pdir.glob('*-seg.nii.gz'))
            if seg_matches:
                entry['label'] = str(seg_matches[0])

        if all_found and len(entry['image']) == 4 and entry['label']:
            records.append(entry)

    print(f'Valid patients with all modalities + seg: {len(records)}')
    return records


def split_data(records):
    train_val, test = train_test_split(
        records, test_size=TEST_RATIO, random_state=SEED)
    train, val = train_test_split(
        train_val, test_size=VAL_RATIO/(1-TEST_RATIO), random_state=SEED)
    print(f'Split → Train: {len(train)} | Val: {len(val)} | Test: {len(test)}')
    return train, val, test


