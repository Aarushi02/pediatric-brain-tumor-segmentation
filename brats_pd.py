def build_file_list(data_dir):
    records = []
    for pdir in sorted(Path(data_dir).iterdir()):
        if not pdir.is_dir(): continue
        pid = pdir.name
        entry = {'image':[], 'label':None}
        ok = True
        for mod in MODALITIES:
            p = pdir / f'{pid}-{mod}.nii.gz'
            if not p.exists():
                m = list(pdir.glob(f'*-{mod}.nii.gz'))
                if m: p = m[0]
                else: ok=False; break
            entry['image'].append(str(p))
        seg = pdir / f'{pid}-seg.nii.gz'
        if not seg.exists():
            m = list(pdir.glob('*-seg.nii.gz'))
            seg = m[0] if m else None
        if ok and len(entry['image'])==4 and seg:
            entry['label'] = str(seg)
            records.append(entry)
    return records

