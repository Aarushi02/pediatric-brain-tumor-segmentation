class CheckpointManager:
    def __init__(self, ckpt_dir, top_k=3):
        self.dir    = Path(ckpt_dir)
        self.dir.mkdir(parents=True, exist_ok=True)
        self.top_k  = top_k
        self.scores = []

        # Load existing checkpoints into memory so top-k tracking works
        for p in sorted(self.dir.glob('*.pth')):
            try:
                score = float(p.stem.split('dice')[-1])
                self.scores.append((score, p))
            except:
                pass
        self.scores.sort(key=lambda x: x[0], reverse=True)
        print(f'CheckpointManager: found {len(self.scores)} existing checkpoints')

    def save(self, model, optimizer, epoch, metrics):
        score = metrics['dice_mean']
        path  = self.dir / f'epoch{epoch:04d}_dice{score:.4f}.pth'
        torch.save({
            'epoch':     epoch,
            'model':     model.state_dict(),
            'optimizer': optimizer.state_dict(),
            'metrics':   metrics,
        }, path)
        self.scores.append((score, path))
        self.scores.sort(key=lambda x: x[0], reverse=True)
        while len(self.scores) > self.top_k:
            _, old = self.scores.pop()
            if old.exists(): old.unlink()
        return path

    def best(self):
        return self.scores[0] if self.scores else (0.0, None)


def compute_peds_dice(pred_oh, label_oh):
    def dice(p, l):
        i = (p * l).sum()
        return (2. * i / (p.sum() + l.sum() + 1e-5)).item()

    wt_p = ((pred_oh[:,1]+pred_oh[:,2]+pred_oh[:,3]+pred_oh[:,4])>0).float()
    wt_l = ((label_oh[:,1]+label_oh[:,2]+label_oh[:,3]+label_oh[:,4])>0).float()
    tc_p = ((pred_oh[:,1]+pred_oh[:,3])>0).float()
    tc_l = ((label_oh[:,1]+label_oh[:,3])>0).float()
    et_p = pred_oh[:,1].float()
    et_l = label_oh[:,1].float()

    return {
        'dice_wt': dice(wt_p, wt_l),
        'dice_tc': dice(tc_p, tc_l),
        'dice_et': dice(et_p, et_l),
    }

