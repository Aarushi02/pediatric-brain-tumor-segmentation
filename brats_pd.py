@torch.no_grad()
def evaluate(model, loader):
    rows = []
    for i, batch in enumerate(tqdm(loader, desc='Evaluating')):
        imgs   = batch['image'].to(DEVICE)
        labels = batch['label'].to(DEVICE)
        preds  = sliding_window_inference(imgs, ROI_SIZE, SW_BATCH, model, overlap=OVERLAP)

        for p, l in zip([post_pred(x)  for x in decollate_batch(preds)],
                        [post_label(x) for x in decollate_batch(labels)]):
            p, l = p.unsqueeze(0), l.unsqueeze(0)
            pw, pt, pe = extract_peds_regions(p)
            lw, lt, le = extract_peds_regions(l)

            row = {'sample': i}
            for tag, pp, ll in [('wt',pw,lw),('tc',pt,lt),('et',pe,le)]:
                pn = pp.squeeze().cpu().numpy().astype(bool)
                ln = ll.squeeze().cpu().numpy().astype(bool)
                row[f'dice_{tag}'] = dice_score(
                    torch.from_numpy(pn.astype(np.float32)),
                    torch.from_numpy(ln.astype(np.float32)))
                row[f'hd95_{tag}'] = hausdorff_95(pn, ln)
                s, sp = sens_spec(pn.astype(float), ln.astype(float))
                row[f'sens_{tag}'] = s; row[f'spec_{tag}'] = sp
            rows.append(row)
    return pd.DataFrame(rows)

